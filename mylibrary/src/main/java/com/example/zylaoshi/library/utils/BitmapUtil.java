    /**
     * @Description: 图片尺寸压缩 把Bitmap保存到指定目录
     * @param image bitmap对象
     * @param filePath 要保存的指定目录
     * @date 2016年3月23日 下午6:28:15
     */
    public static void compressBitmap(Bitmap image, int maxSize, File filePath) {
        // 最大图片大小 150KB
        maxSize = 150;
        // 获取尺寸压缩倍数
        int ratio = BitmapUtil.getRatioSize(image.getWidth(), image.getHeight());
        // 压缩Bitmap到对应尺寸
        Bitmap result = Bitmap.createBitmap(image.getWidth() / ratio, image.getHeight() / ratio, Bitmap.Config.ARGB_8888);
        Canvas canvas = new Canvas(result);
        Rect rect = new Rect(0, 0, image.getWidth() / ratio, image.getHeight() / ratio);
        canvas.drawBitmap(image, null, rect, null);
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        // 质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
        int options = 100;
        result.compress(Bitmap.CompressFormat.JPEG, options, baos);
        // 循环判断如果压缩后图片是否大于100kb,大于继续压缩
        while (baos.toByteArray().length / 1024 > maxSize) {
            // 重置baos即清空baos
            baos.reset();
            // 每次都减少10
            options -= 10;
            // 这里压缩options%，把压缩后的数据存放到baos中
            result.compress(Bitmap.CompressFormat.JPEG, options, baos);
        }
        saveToFileOrFilePath(result, filePath);
        // 释放Bitmap
        if (!result.isRecycled()) {
            result.recycle();
        }
    }

    public static void saveToFileOrFilePath(Bitmap bitmap, File folder){
        if (folder != null) {
            saveToFileOrFilePath(bitmap,folder.getAbsolutePath());
        }
    }

    /**
     * @Description: 图片压缩 把Bitmap保存到指定目录
     * @param curFilePath 当前图片文件地址
     * @param targetFilePath 要保存的图片文件地址
     * @date 2016年9月28日 下午17:43:15
     */
    public static void compressBitmap(String curFilePath, int maxSize,File targetFilePath) {
        // 最大图片大小 150KB
         maxSize = 150;
        //根据地址获取bitmap
        Bitmap result = getBitmapFromFile(curFilePath);
        ByteArrayOutputStream baos = new ByteArrayOutputStream();
        // 质量压缩方法，这里100表示不压缩，把压缩后的数据存放到baos中
        int quality = 100;
        result.compress(Bitmap.CompressFormat.JPEG, quality, baos);
        // 循环判断如果压缩后图片是否大于100kb,大于继续压缩
        while (baos.toByteArray().length / 1024 > maxSize) {
            // 重置baos即清空baos
            baos.reset();
            // 每次都减少10
            quality -= 10;
            // 这里压缩quality，把压缩后的数据存放到baos中
            result.compress(Bitmap.CompressFormat.JPEG, quality, baos);
        }
        saveToFileOrFilePath(result,targetFilePath);
        // 释放Bitmap
        if (!result.isRecycled()) {
            result.recycle();
        }

    }

